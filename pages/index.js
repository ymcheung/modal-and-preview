import Head from 'next/head';
import { useState, useReducer } from 'react';
import styles from '../styles/Home.module.css';

import Dialog from './components/Dialog';

export default function Home() {
  const formReducer = (state, action) => {
    switch (action.type) {
      case 'OPEN_DIALOG':
        return {
          ...state,
          isOpen: true,
          title: action.payload.title
        };

      case 'CLOSE_DIALOG':
        return {
          ...state,
          isOpen: false,
          title: ''
        };

      default:
        return state;
    }
  };

  const [state, dispatch] = useReducer(formReducer, {
    isOpen: false,
    title: ''
  });

  const handleOpenDialog = (dialogTitle) => {
    dispatch({
      type: 'OPEN_DIALOG',
      payload: {
        title: dialogTitle
      }
    });
  };

  const Sections = () => {
    const sections = [
      {
        button: 'Upload Logo',
        dialogTitle: 'Logo'
      },
      {
        button: 'Upload Cover Logo',
        dialogTitle: 'Cover Photo'
      },
      {
        button: 'Input Quiz Name',
        dialogTitle: 'Text Input'
      },
      {
        button: 'Background',
        dialogTitle: 'Background Color/Image'
      }
    ];

    return (
      <>
        {sections.map(({ button, dialogTitle, type }, index) => {
          return (
            <section className={styles.section} key={index}>
              <h2>preview text/image</h2>
              {/* if (!pre) return button */}
              <button
                type="button"
                onClick={() => handleOpenDialog(dialogTitle)}
              >
                {button}
              </button>
            </section>
          );
        })}
      </>
    );
  };

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <header>Cover Photo</header>
      <main className={styles.container}>
        <Sections />
      </main>
      <Dialog
        isOpen={state.isOpen}
        dialogTitle={state.title}
        close={dispatch}
      />
    </>
  );
}
