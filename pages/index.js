import Head from 'next/head';
import { useState, useReducer } from 'react';
import styles from '../styles/Home.module.css';

import Dialog from './components/Dialog';

export default function Home() {
  const dialogReducer = (state, action) => {
    switch (action.type) {
      case 'OPEN_DIALOG':
        return {
          ...state,
          isOpen: true,
          title: action.payload.title,
          inputs: action.payload.inputs,
          onClose: action.payload.onClose
        };

      case 'CLOSE_DIALOG':
        return {
          ...state,
          isOpen: false,
          title: '',
          inputs: []
        };

      default:
        return state;
    }
  };

  const [dialogState, dispatchDialog] = useReducer(dialogReducer, {
    isOpen: false,
    title: '',
    inputs: []
  });

  const handleOpenDialog = (dialogTitle, inputs) => {
    dispatchDialog({
      type: 'OPEN_DIALOG',
      payload: {
        title: dialogTitle,
        inputs: inputs,
        onClose: false
      }
    });
  };

  const previewReducer = (state, action) => {
    switch (action.type) {
      case 'QUIZ_NAME':
        return {
          ...state,
          quizName: action.payload.quizName
        };

      default:
        return state;
    }
  };

  const [previewState, dispatchPreview] = useReducer(previewReducer, {
    quizName: ''
  });

  const Sections = () => {
    const sections = [
      {
        button: 'Upload Logo',
        dialogTitle: 'Logo',
        inputs: [
          {
            type: 'file',
            name: 'logo',
            accept: 'image/*'
          }
        ]
      },
      {
        button: 'Upload Cover Photo',
        dialogTitle: 'Cover Photo',
        inputs: [
          {
            type: 'file',
            name: 'cover',
            accept: 'image/*'
          }
        ]
      },
      {
        button: 'Input Quiz Name',
        dialogTitle: 'Text Input',
        inputs: [
          {
            type: 'text',
            name: 'quizName'
          }
        ]
      },
      {
        button: 'Background',
        dialogTitle: 'Background Color/Image',
        inputs: [
          {
            type: 'color',
            name: 'fill'
          }
        ]
      }
    ];

    return (
      <>
        {sections.map(({ button, dialogTitle, inputs }, index) => {
          return (
            <section className={styles.section} key={index}>
              {inputs.map(({ name }) => previewState[name])}
              <button
                type="button"
                onClick={() => handleOpenDialog(dialogTitle, inputs)}
              >
                {button}
              </button>
            </section>
          );
        })}
      </>
    );
  };

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <header>Cover Photo</header>
      <main className={styles.container}>
        <Sections />
      </main>
      <Dialog
        isOpen={dialogState.isOpen}
        dispatchDialog={dispatchDialog}
        dialogTitle={dialogState.title}
        inputs={dialogState.inputs}
        dispatchPreview={dispatchPreview}
      />
    </>
  );
}
